public class Cyouhoukeyi {
    public static void main(String[] args) {
        cyouhoukeyiNoSyuuNoRagasa(5, 6);
    }

    public static void cyouhoukeyiNoSyuuNoRagasa(double nagasa, double haba) {
        double syuucyou = (nagasa + haba) * 2;
        System.out.println(syuucyou);
    }
}
